export const getTop10: () => ContentItem[] = () => {
  return mockMovies
    .sort((a, b) => b.views_count - a.views_count)
    .slice(0, 10)
    .map((movie) => ({
      id: movie.id,
      title: movie.title,
      poster_url: movie.poster_url,
      backdrop_url: movie.backdrop_url,
      category: movie.category,
      type: 'MOVIE' as const,
      stream_url: movie.stream_url,
      description: movie.description,
      year: movie.year,
      duration: movie.duration,
      rating: movie.rating,
      views_count: movie.views_count,
    }));
};

export const getMoviesByCategory = (category: string): ContentItem[] => {
  return mockMovies
    .filter((movie) => movie.category === category)
    .map((movie) => ({
      id: movie.id,
      title: movie.title,
      poster_url: movie.poster_url,
      backdrop_url: movie.backdrop_url,
      category: movie.category,
      type: 'MOVIE' as const,
      stream_url: movie.stream_url,
      description: movie.description,
      year: movie.year,
      duration: movie.duration,
      rating: movie.rating,
      views_count: movie.views_count,
    }));
};

